package com.luguosong.basic;

/**
 * 相等与关系运算符示例
 *
 * @author luguosong
 */
public class ComparisonDemo {

	public static void main(String[] args) {
		int value1 = 1;
		int value2 = 2;
		if (value1 == value2)
			System.out.println("value1 == value2");
		if (value1 != value2)
			System.out.println("value1 != value2");
		if (value1 > value2)
			System.out.println("value1 > value2");
		if (value1 < value2)
			System.out.println("value1 < value2");
		if (value1 <= value2)
			System.out.println("value1 <= value2");
	}
}
